function getRandomWord(){
  return words[Math.floor(Math.random() * words.length)];
}

function NuevoJuego(){
    word = getRandomWord();
    guess= [];
    attempts= [];
    output.innerHTML = '';
    resetearColores();
    ActualizarP();
}

function resetearColores() {
  for (let i = 1; i <= 26; i++) {
    let boton = document.getElementById('miBoton' + String.fromCharCode(64 + i));
    boton.classList.remove('botonRojo-' + i);
    boton.classList.add('botonAzul-' + i);
  }
}

function ActualizarP(){
    var current = '';
    input.value = '';
    for(i=0; i<word.length; i++){
        if (word[i] == ' '){
            current += '<br>';
        } else if(guess.indexOf(word[i]) !== -1){
            current += '<span>' + word[i]+'</span>';
        }
        else
        {
            current += '<span> </span>';
        }
    }
    h2.innerHTML = current;
    output.innerHTML = man[attempts.length];
    if(current.indexOf('> <') == -1){
        alert("¡¡Ganaste!!");
    }
    if(attempts.length>=7){
        alert("Perdiste la palabra era: "+word);
    }

    input.focus()
}

function AdivinarL(){
    if(input.value !== undefined){
        if(attempts.length < 7){
            if(word.indexOf(input.value)!== -1){
                alert("Correcto");
                guess.push(input.value.toLowerCase());
            }else{
                attempts.push(input.value.toLowerCase());
                alert("Incorrecto,"+"Te Quedan: "+attempts.length+"/7");
            }
            ActualizarP();
        }
    }
}
